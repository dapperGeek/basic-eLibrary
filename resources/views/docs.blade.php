            <p>
                <h3>Account Creation</h3>
                To access the library, a user must be create an account. <br> <br>

                * User Registration page <a href="http://app.elastic250.com/register">http://app.elastic250.com/register</a> <br><br>
                
                * User Login page <a href="http://app.elastic250.com/login">http://app.elastic250.com/login</a> <br><br>

                A user can create an account either as a <b>Librarian</b> or <b>Reader</b>. Normally, the Librarian role would be assigned but for test purposes, the option is included on registration.<br><br>

                Once registered, user is redirected to the library home page, where the list of books is displayed. To view book details, a link is available on the <i>Book Title</i>. <br><br>

                <h3>Add Book</h3>
                Librarians have access to the <b>"Add Book"</b> button, at the top right of home page, to add new books. <br><br>

                <h3>Check-Out & Check-In</h3>
                Both librarians and readers are able to check out books. To check-out a book, the book detail page has to be opened, via <b>Title</b> link, to reveal the check out option. Already checked out books will display a Check-In option <br><br>


